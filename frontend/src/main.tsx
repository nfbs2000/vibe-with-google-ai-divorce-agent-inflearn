import React from 'react';
import ReactDOM from 'react-dom/client';
import { QueryClient, QueryClientProvider } from '@tanstack/react-query';
import App from './App.tsx';
import './index.css';
import { initializeFirebase, activateRemoteConfig } from './lib/firebase';

// Firebase Remote Config 초기화 (variant 관리용)
initializeFirebase();
activateRemoteConfig().catch(error => {
  console.warn('Firebase Remote Config activation failed:', error);
});

// React Query 클라이언트 설정
const queryClient = new QueryClient({
  defaultOptions: {
    queries: {
      retry: 1,
      refetchOnWindowFocus: false,
      staleTime: 5 * 60 * 1000, // 5분
    },
  },
});

// React 18의 새로운 createRoot API 사용
ReactDOM.createRoot(document.getElementById('root')!).render(
  <React.StrictMode>
    <QueryClientProvider client={queryClient}>
      <App />
    </QueryClientProvider>
  </React.StrictMode>,
);